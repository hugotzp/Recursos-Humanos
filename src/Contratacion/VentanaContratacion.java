    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        Persona persona = new Personal();
        persona.setNombre(jTextFieldNombreAspirante.getText());
        persona.setApellido(jTextFieldApellidosAspirante.getText());
        persona.setDPI(jTextFieldDPIAspirante.getText());
        if(jComboBoxGeneroAspirante.getSelectedItem()=="Femenino"){
            persona.setGenero(false);
        }
        else{
            persona.setGenero(true);
        }
        persona.setFechaNacimiento(jDateChooserFNacimiento.getDate());
        persona.setNumeroTelefonico(jTextFieldTelefonoAspirante.getText());
        
        PersonasInteresadas aspirante = new Aspirantes(persona);        
        aspirante.setTipoProceso(true);
        
        /*aspirante.setEnProceso(true);
        aspirante.setSalarioEsperado(Float.valueOf(jTextFieldSalarioEAspirante.getText()));*/
        
        jTextFieldNombreAspirante.setText("");
        jTextFieldApellidosAspirante.setText("");
        jTextFieldDPIAspirante.setText("");
        jDateChooserFNacimiento.setDateFormatString("");
        jTextFieldTelefonoAspirante.setText("");
        jTextFieldSalarioEAspirante.setText("");
        
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed

